#include<iostream>
using namespace std;
int main (){
    int a;
    // USE OF FOR LOOP
    for(a=1; a<=100; a++){
        cout<<a<<endl;
    }

    // USE OF WHILE LOOP

    int m = 1;

    while(m<=40){

        cout<<m<<endl;
        m++;

    }

    // DO WHILE LOOP

    int v = 1;
    do{
        cout<<v<<endl;
        v++;    
    }while(v<=10);

     




    return 0; 
}
